# Python Rounding, Absolute Value, and Exponents

# round()
myGPA = 3.6
print(round(myGPA))
amountOfSalt = 1.4
print(round(amountOfSalt))

apple = -1.2
print(round(apple))
google = -1.6
print(round(google))

# abs()
distanceAway = -13
print(abs(distanceAway))
lengthOfRootInGround = -2.5
print(abs(lengthOfRootInGround))

# pow()
chanceOfTails = 0.5
inARowTails = 3
#chance of getting a tail 3 times in a row
print(pow(chanceOfTails, inARowTails))
print(chanceOfTails**inARowTails)
chanceOfOne = .167
inARowOne = 2
#chance of rolling 1 in dice 2 times in a row
print(pow(chanceOfOne, inARowOne))